# Сложность O(n) - линейная сложность:
# Описание: Время выполнения алгоритма прямо пропорционально размеру входных данных.
# Примеры: Линейный поиск в неотсортированном массиве, обход всех элементов в связном списке.

def linear_algo(arr):
    for num in arr:
        print(num)


# Пример использования:
my_list = [1, 2, 3, 4, 5]
linear_algo(my_list)
# Вывод:
# 1
# 2
# 3
# 4
# 5
