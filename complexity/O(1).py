# Сложность O(1) - постоянная сложность:
# Описание: Алгоритм имеет постоянное время выполнения, независимо от размера входных данных.
# Примеры: Получение элемента по индексу в массиве, выполнение арифметических операций.

def constant_algo(arr):
    return arr[0]


# Пример использования:
my_list = [1, 2, 3, 4, 5]
result = constant_algo(my_list)
print(result)  # Вывод: 1
